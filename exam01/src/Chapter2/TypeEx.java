package chapter2;

public class TypeEx {
	public static void main(String[] args) {
		var num1 = 10;	// int
		var num2 = 3.14;	//double
		var text = "ABCD";	// String 문자열
		
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(text);
		
		// text = 10;
		text = "ABCDEFG";
	}
}
